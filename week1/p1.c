#include <stdio.h>
int main() {
   
   // Print the required messages.
   printf("C uses escape sequences for a variety of purposes.\n");
   printf("Some common ones are:\n");

   // Desirable output
   // to print " , use \"
   // to print \ , use \\
   // to jump to a new line , use \n

   printf("to print \", use \\\"\n");
   printf("to print \\, use \\\\\n");
   printf("to jump to a new line, use \\n \n");

   return 0;
}

